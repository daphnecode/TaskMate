// 특정 레벨의 경험치 가져오기
export function getLevelExp(level: number): number {
  if (level < 1) return 0;
  if (level > petLevelTable.length) {
    return petLevelTable[petLevelTable.length - 1].totalExp;
  }
  return petLevelTable[level - 1].totalExp;
}
